#include <unistd.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb == 0 || nb < 0)
		return (0);
	while (i * i != nb)
	{
		if (i * i > nb)
		{
			return (0);
		}
		i++;
	}
	if (i * i == nb)
	{
		return (i);
	}
	return (0);
}
